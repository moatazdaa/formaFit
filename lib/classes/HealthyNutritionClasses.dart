class Meal {
  final String MealName;
  final DateTime Daydate;
  final DateTime MealTime;
  final double MealKal;
  final double CompletKal;
  bool isfindfood;

  Meal({
    required this.MealName,
    DateTime? Daydate,
    required this.MealTime,
    required double MealKal, // تأكد من أن هذا المتغير من النوع double
    required this.CompletKal,
    required this.isfindfood,
  })  : this.Daydate = Daydate ?? DateTime.now(),
        this.MealKal = MealKal.toDouble(); // تحويل إلى double عند الإنشاء
}

